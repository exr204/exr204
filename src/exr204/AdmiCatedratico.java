    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        NodoCatedratico temporal = inicio;
        if(temporal != null)
        {
            for(int i = 0; i < jTable2.getRowCount(); i++) {
                    
                    temporal.id = jTable2.getValueAt(i, 0).toString();
                    temporal.nombre = jTable2.getValueAt(i, 1).toString();
                    temporal.contrasenia = jTable2.getValueAt(i, 2).toString();
                    
                
                temporal = temporal.siguiente;
            }
        }
        
        JOptionPane.showMessageDialog(null, "Se han guardado los cambios correctamente");
    }//GEN-LAST:event_jButton3ActionPerformed

